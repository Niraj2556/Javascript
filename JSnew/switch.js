// switch case
let day =2;
switch(day){
    case 0:
        console.log("monday");
        break;
    
        case 1 :
            console.log("tuesday");
            break;

        case 2:
            console.log("wednesday");
            break;
        case 3 :
            console.log("thursday");
            break;
        default:
            console.log("invalid input" );

}
