// trim()
// toUpperCase()
// toLowerCase()
// slice()


let name ="    Jennifer     "
console.log(name);

// trim() is use to remove the side spaces from string
console.log(name.trim())


// toUpperCase() is use to convert the whole string in the upper case
console.log(name.toUpperCase())


// toLowerCase is just like uppercase but convert string in the lower case
console.log(name.toLowerCase())



// slice() is use to print the sliced part of the string by their index
let jack ="jack & Jennifer"
console.log(jack.slice(0,4))


