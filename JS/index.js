// first index element of any string , by increasing the number of index "access any index"
var name = "udbcjhdsbcj";
namelength= name[0]// to find first index , do not find length of the string 
console.log(namelength)

// last index element of any string
var name = "udbcjhdsbcj";
namelength=name[name.length-1]// to find last index , you have to find length of the string 
console.log(namelength)

